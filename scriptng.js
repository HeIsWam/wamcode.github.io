function showMenu() {
  var x = document.getElementById("nav-bar")
  x.style.display = "block";
  var y = document.getElementById("close-menu-bar")
  y.style.display = "block";
}
function closeMenu() {
  var x = document.getElementById("nav-bar")
  x.style.display = "none";
  var y = document.getElementById("close-menu-bar")
  y.style.display = "none";
}

